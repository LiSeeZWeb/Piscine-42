#include <unistd.h>

int main(int argc, char **argv)
{
	int	i;

	i = -1;
	if (argc == 2)
	{
		while(argv[1][++i] != '\0')
		{
			if (argv[1][i] >= 'a' && argv[1][i] <= 'y')
				argv[1][i] += 1;
			else
				argv[1][i] -= 25;
			write(1, &argv[1][i], 1);
		}
	}
	write(1, "\n", 1);
}
